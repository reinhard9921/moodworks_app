class User {

  int userID;
  String firstname;
  String lastname;
  String email;
  String password;
  String age;

  User({int userID, String firstname, String lastname, String email, String password, String age}){
    this.userID = userID;
    this.firstname = firstname;
    this.lastname = lastname;
    this.email = email;
    this.password = password;
    this.age = age;

  }

}